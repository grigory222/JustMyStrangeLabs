SELECT Н_ЛЮДИ.ИД, Н_СЕССИЯ.УЧГОД FROM Н_ЛЮДИ
JOIN Н_СЕССИЯ ON Н_СЕССИЯ.ЧЛВК_ИД=Н_ЛЮДИ.ИД
WHERE Н_ЛЮДИ.ФАМИЛИЯ < 'Иванов' AND Н_СЕССИЯ.ИД = 1975;

SELECT Н_ЛЮДИ.ОТЧЕСТВО, Н_ОБУЧЕНИЯ.НЗК, Н_УЧЕНИКИ.ГРУППА FROM Н_ЛЮДИ
LEFT JOIN Н_ОБУЧЕНИЯ ON Н_ОБУЧЕНИЯ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
LEFT JOIN Н_УЧЕНИКИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
WHERE Н_ЛЮДИ.ИМЯ = 'Ярослав' AND Н_ОБУЧЕНИЯ.ЧЛВК_ИД = 112514 AND Н_УЧЕНИКИ.ИД = 150308;
