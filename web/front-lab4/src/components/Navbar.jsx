import AppBar from '@mui/material/AppBar';
import Box from '@mui/material/Box';
import Toolbar from '@mui/material/Toolbar';
import Typography from '@mui/material/Typography';
import Button from '@mui/material/Button';
import IconButton from '@mui/material/IconButton';
import MenuIcon from '@mui/icons-material/Menu';
import {Avatar, Container, Menu, MenuItem, Tooltip} from "@mui/material";
import HomeIcon from '@mui/icons-material/Home';
import {useState} from "react";
import {Link as RouterLink} from 'react-router-dom';
import {useDispatch, useSelector} from "react-redux";
import {useLogoutMutation} from "../api/myLegendaryApi.js";
import {setLoggedIn} from "../storage/IsLoggedSlice.js";
import {clearResults} from "../storage/ResultsSlice.js";


const pages = ['Главная'];
const settings = ['Выход'];
const pageRoutes = {
    [pages[0]]: "/main",
    [settings[0]]: 'logout'
};

export function Navbar() {
    const [anchorElNav, setAnchorElNav] = useState(null);
    const [anchorElUser, setAnchorElUser] = useState(null);
    const dispatch = useDispatch();
    const [logout] = useLogoutMutation();
    const isLogged = useSelector(state => state.reducer.auth.isLogged);

    const handleOpenNavMenu = (event) => {
        setAnchorElNav(event.currentTarget);
    };

    const handleOpenUserMenu = (event) => {
        setAnchorElUser(event.currentTarget);
    };

    const handleCloseNavMenu = () => {
        setAnchorElNav(null);
    };

    const handleCloseUserMenu = async (setting) => {
        if (setting === 'Выход') {
            try {
                const payload = await logout().unwrap()
                console.log("payload", payload)
                dispatch(setLoggedIn(false));
                console.log("clearing results")
                dispatch(clearResults());
                console.log("clearing results2")
            } catch (error) {
                console.log(error.data.error);
            }
        }
        setAnchorElUser(null);
    };

    return (
        <AppBar position="static">
            <Container maxWidth="xl">
                <Toolbar disableGutters>
                    <HomeIcon sx={{display: {xs: 'none', md: 'flex'}, mr: 1}}/>
                    <Typography
                        variant="h6"
                        noWrap
                        component="a"
                        sx={{
                            mr: 2,
                            display: {xs: 'none', md: 'flex'},
                            fontWeight: 300,
                            letterSpacing: '.1rem',
                            color: 'inherit',
                        }}
                    >
                        ЛР №4
                    </Typography>

                    <Box sx={{flexGrow: 1, display: {xs: 'flex', md: 'none'}}}>
                        <IconButton
                            size="large"
                            aria-label="account of current user"
                            aria-controls="menu-appbar"
                            aria-haspopup="true"
                            onClick={handleOpenNavMenu}
                            color="inherit"
                        >
                            <MenuIcon/>
                        </IconButton>
                        <Menu
                            id="menu-appbar"
                            anchorEl={anchorElNav}
                            anchorOrigin={{
                                vertical: 'bottom',
                                horizontal: 'left',
                            }}
                            keepMounted
                            transformOrigin={{
                                vertical: 'top',
                                horizontal: 'left',
                            }}
                            open={Boolean(anchorElNav)}
                            onClose={handleCloseNavMenu}
                            sx={{display: {xs: 'block', md: 'none'}}}
                        >
                            {pages.map((page) => (
                                <MenuItem key={page} onClick={handleCloseNavMenu}>
                                    <Button
                                        component={RouterLink}
                                        to={pageRoutes[page]}
                                        onClick={handleCloseNavMenu}
                                        sx={{color: 'inherit', textTransform: 'none'}}
                                    >
                                        {page}
                                    </Button>
                                </MenuItem>
                            ))}
                        </Menu>
                    </Box>
                    <Typography
                        variant="h5"
                        noWrap
                        component="a"
                        href="#app-bar-with-responsive-menu"
                        sx={{
                            mr: 2,
                            display: {xs: 'flex', md: 'none'},
                            flexGrow: 1,
                            fontWeight: 300,
                            letterSpacing: '.1rem',
                            color: 'inherit',
                            textDecoration: 'none'
                        }}
                    >
                        ЛР №4
                    </Typography>
                    <Box sx={{flexGrow: 1, display: {xs: 'none', md: 'flex'}}}>
                        {pages.map((page) => (
                            <Button
                                key={page}
                                component={RouterLink}
                                to={pageRoutes[page]}
                                onClick={handleCloseNavMenu}
                                sx={{my: 2, color: 'white', display: 'block'}}
                            >
                                {page}
                            </Button>
                        ))}
                    </Box>
                    <Box sx={{flexGrow: 0}}>
                        <Tooltip title="Open settings">
                            <IconButton onClick={handleOpenUserMenu} sx={{p: 0}}>
                                <Avatar alt="123" src="/static/images/man.png"/>
                            </IconButton>
                        </Tooltip>
                        {!isLogged ? null :
                            <Menu
                                sx={{mt: '45px'}}
                                id="menu-appbar"
                                anchorEl={anchorElUser}
                                anchorOrigin={{
                                    vertical: 'top',
                                    horizontal: 'right',
                                }}
                                keepMounted
                                transformOrigin={{
                                    vertical: 'top',
                                    horizontal: 'right',
                                }}
                                open={Boolean(anchorElUser)}
                                onClose={handleCloseUserMenu}
                            >
                                {settings.map((setting) => (
                                    <MenuItem key={setting} onClick={() => handleCloseUserMenu(setting)}>
                                        <Button
                                            component={RouterLink}
                                            sx={{color: 'inherit', textTransform: 'none'}}
                                        >
                                            {setting}
                                        </Button>
                                    </MenuItem>
                                ))}
                            </Menu>}
                    </Box>
                </Toolbar>
            </Container>
        </AppBar>
    );
}